#!/usr/bin/env python3
"""
protocol_akn_vertex_stars.py - Enumerate AKN local vertex-star configurations.

The Ammann-Kramer-Neri (AKN) icosahedral tiling, generated by cut-and-project
from Z^6 with the canonical M_parallel / M_perp projection matrices and the
Rhombic Triacontahedron (RT) acceptance window in E_perp, permits a finite
set of distinct local vertex configurations. App Z Sec Z.3 cites the count
as 32 (16 + 16 with parity, per the rge_native.py header).

Algorithm
---------
1. Generate a large patch of the AKN tiling by cut-and-project.
2. For each interior vertex v in the patch:
     - For each of the 12 displacements d = +/- e_i (i = 1..6) in Z^6:
       - Test whether v + d is in the patch (i.e., whether its perp-projection
         lies inside the RT acceptance window).
       - Record a single bit per displacement -> 12-bit "presence vector."
3. Two presence vectors are equivalent under the action of the icosahedral
   point group I_h on the 6 basis vectors (60 rotations from I plus parity
   inversion -> 120 elements).
4. Count the equivalence classes (orbits) and verify the count = 32.

The enumeration provides:
  - The list of 32 representative 12-bit vectors (one per orbit).
  - The coordination number (popcount) histogram per orbit.
  - The orbit size per representative.

This catalog is the foundation for QLQCD-1L (App Z): Monte Carlo updates of
the gauge link field on the AKN tiling must respect the matching rules
encoded by this catalog (Theta(v in V_32) indicator in Eq. Z.3.1).
"""

