import sys
from itertools import product
from pathlib import Path

import numpy as np

ENGINE_ROOT = Path(__file__).resolve().parent.parent
SRC_PATH = ENGINE_ROOT / "src"
sys.path.insert(0, str(SRC_PATH))

from gct_projections import get_m_perp
from protocol_cage_repair import (
    vertex_pairs_from_projection,
    icosahedral_rotations_lattice_frame,
    lift_to_6d_signed_perm,
)


# AKN acceptance window: vertex v in Z^6 belongs to the AKN tiling iff
# its perp-projection v_perp lies inside the standard AKN window W.
# W is the perp-projection of the unit 6-cube [-1/2, 1/2]^6, which is
# the rhombic triacontahedron (RT) centred at origin in E_perp.
#
# The RT has 30 rhombic faces, but the convex hull built from the 64
# cube-vertex projections is convex with the correct face structure.
# We test membership via the convex-hull half-space inequalities
# (computed once at module load via scipy.spatial.ConvexHull).

def akn_window_halfspaces() -> tuple[np.ndarray, np.ndarray]:
    """Compute the half-space inequalities A x <= b defining the AKN
    acceptance window W in E_perp.

    W = perp-projection of the centred unit 6-cube [-1/2, 1/2]^6 to E_perp.
    The convex hull of the 64 perp-projected cube vertices is the
    rhombic triacontahedron W. The convex-hull half-space inequalities
    (A, b) characterise membership: x in W iff A @ x <= b for all rows.
    """
    from scipy.spatial import ConvexHull

    M_perp = get_m_perp()
    # 64 vertices of the centred unit 6-cube
    cube_vertices = np.array(list(product([-0.5, 0.5], repeat=6)))
    perp_images = cube_vertices @ M_perp.T   # (64, 3)
    hull = ConvexHull(perp_images)
    # hull.equations rows: [A_i, ..., -b_i] with A_i x + (-b_i) <= 0
    # so A x <= b with A = hull.equations[:, :-1], b = -hull.equations[:, -1]
    A = hull.equations[:, :-1]
    b = -hull.equations[:, -1]
    return A, b


def is_in_akn_window(v_perp: np.ndarray, A: np.ndarray, b: np.ndarray,
                     tol: float = 1e-9) -> bool:
    """Test whether v_perp is inside the canonical AKN window W."""
    return bool(np.all(A @ v_perp <= b + tol))


def generate_akn_patch(R: int) -> set[tuple[int, ...]]:
    """Generate all Z^6 points x with max|x_i| <= R whose perp-projection
    lies inside the canonical AKN window W.

    Returns a set of 6-tuples (integer coords).
    """
    M_perp = get_m_perp()
    A, b = akn_window_halfspaces()

    patch = set()
    coords = range(-R, R + 1)
    for x in product(coords, repeat=6):
        v6 = np.array(x, dtype=np.float64)
        v_perp = M_perp @ v6
        if is_in_akn_window(v_perp, A, b):
            patch.add(x)
    return patch


# The 12 nearest-neighbour displacements in Z^6:
#   +e_1, +e_2, ..., +e_6, -e_1, -e_2, ..., -e_6
# Bit positions 0..5  -> +e_1..+e_6
# Bit positions 6..11 -> -e_1..-e_6
def displacement_basis() -> list[tuple[int, ...]]:
    """Return the 12 unit displacements as 6-tuples."""
    disps = []
    for i in range(6):
        d = [0] * 6
        d[i] = 1
        disps.append(tuple(d))
    for i in range(6):
        d = [0] * 6
        d[i] = -1
        disps.append(tuple(d))
    return disps


def compute_presence_vectors(patch: set[tuple[int, ...]],
                              interior_margin: int = 1) -> dict[int, list[tuple[int, ...]]]:
    """For each interior vertex of the patch (one not on the boundary in any
    direction), compute its 12-bit presence vector indicating which of the
    12 nearest neighbours are also in the patch.

    Returns a dict mapping each distinct 12-bit presence pattern (as int) to
    the list of vertices realising it.
    """
    disps = displacement_basis()
    by_pattern: dict[int, list[tuple[int, ...]]] = {}
    for v in patch:
        # Interior check: every displaced vertex must also be in the patch
        # for the presence vector to be unambiguous (no patch-edge artifacts).
        # We enforce that ALL 12 neighbours are within the bounding box, even
        # if not within the AKN window. interior_margin gives the required
        # bounding-box margin.
        max_abs = max(abs(c) for c in v)
        if max_abs > max(abs(c) for c in v) + interior_margin:
            continue
        bits = 0
        for k, d in enumerate(disps):
            neighbour = tuple(v[i] + d[i] for i in range(6))
            if neighbour in patch:
                bits |= (1 << k)
        by_pattern.setdefault(bits, []).append(v)
    return by_pattern


def signed_perm_acts_on_12bit(M: np.ndarray, pattern: int) -> int:
    """Apply a 6x6 signed-permutation I-rotation to a 12-bit presence vector.

    The 12 bit positions correspond to displacements (+e_1..+e_6, -e_1..-e_6).
    A signed permutation M sends +e_i to sign * e_{j} for some (j, sign);
    this maps bit k (corresponding to displacement d_k) to a new bit
    position k' (corresponding to displacement M @ d_k).
    """
    disps = displacement_basis()
    # Build forward map: bit position k -> new bit position k'
    new_pattern = 0
    for k in range(12):
        d = np.array(disps[k], dtype=np.float64)
        Md = M @ d
        # Find which displacement Md equals
        for kp, dp in enumerate(disps):
            if all(abs(Md[i] - dp[i]) < 1e-7 for i in range(6)):
                if (pattern >> k) & 1:
                    new_pattern |= (1 << kp)
                break
        else:
            raise RuntimeError(f"Signed-permutation image of basis displacement {disps[k]} not found.")
    return new_pattern


def build_Ih_group_on_Z6() -> list[np.ndarray]:
    """Return the 120 6x6 signed-permutation matrices of I_h acting on Z^6.

    Combines the 60 rotations from I (via lifting the lattice-frame icosahedral
    rotations) with parity inversion (the central element -I_6 of full I_h).
    """
    vertices_perp, _ = vertex_pairs_from_projection()
    rots_3d = icosahedral_rotations_lattice_frame(vertices_perp)
    group_60 = []
    for R in rots_3d:
        M = lift_to_6d_signed_perm(R, vertices_perp)
        if M is None:
            raise RuntimeError("Failed to lift an I rotation to Z^6.")
        group_60.append(M)
    if len(group_60) != 60:
        raise RuntimeError(f"Expected 60 lifted matrices, got {len(group_60)}.")
    # Full I_h = I times {+I, -I}: append the parity-flipped versions
    minus_I6 = -np.eye(6)
    group_120 = group_60 + [minus_I6 @ M for M in group_60]
    return group_120


def orbits_under_group(patterns: list[int], group: list[np.ndarray]) -> list[set[int]]:
    """Partition a list of 12-bit patterns into orbits under the action of
    a group of 6x6 signed-permutation matrices."""
    remaining = set(patterns)
    orbits = []
    while remaining:
        seed = next(iter(remaining))
        orbit = {seed}
        frontier = {seed}
        while frontier:
            new_frontier = set()
            for p in frontier:
                for M in group:
                    q = signed_perm_acts_on_12bit(M, p)
                    if q not in orbit:
                        orbit.add(q)
                        new_frontier.add(q)
            frontier = new_frontier
        orbits.append(orbit)
        remaining -= orbit
    return orbits


def coordination(pattern: int) -> int:
    """Popcount of a 12-bit pattern = coordination number (number of nearest
    AKN neighbours at that vertex)."""
    return bin(pattern).count("1")


def main():
    print("=" * 76)
    print("AKN Vertex-Star Enumeration (App Z Sec Z.3 catalog)")
    print("=" * 76)

    print("\n--- Step 1: Generate AKN patch by cut-and-project ---")
    R = 5  # Z^6 bounding box; (2R+1)^6 = 11^6 = 1771561 candidates
    patch = generate_akn_patch(R)
    print(f"  Z^6 bounding box R = {R}; {(2*R+1)**6} candidate points")
    print(f"  Surviving AKN window: {len(patch)} vertices")

    print("\n--- Step 2: Compute 12-bit presence vector at each interior vertex ---")
    # Interior = any vertex such that all 12 neighbours stay within +/- R bounding box
    interior_patch = {
        v for v in patch if all(abs(c) <= R - 1 for c in v)
    }
    by_pattern = {}
    disps = displacement_basis()
    for v in interior_patch:
        bits = 0
        for k, d in enumerate(disps):
            neighbour = tuple(v[i] + d[i] for i in range(6))
            if neighbour in patch:
                bits |= (1 << k)
        by_pattern.setdefault(bits, []).append(v)
    print(f"  Interior vertices (all 12 neighbours in bounding box): {len(interior_patch)}")
    print(f"  Distinct raw 12-bit patterns realised: {len(by_pattern)}")

    print("\n--- Step 3: Build I_h group on Z^6 (120 signed permutations) ---")
    group = build_Ih_group_on_Z6()
    print(f"  I_h order verified: {len(group)} (expected 120)")
    group_I_only = group[:60]
    print(f"  I (rotations only) subgroup: {len(group_I_only)} (expected 60)")

    print("\n--- Step 4: Partition realised patterns into I_h orbits ---")
    patterns = list(by_pattern.keys())
    orbits = orbits_under_group(patterns, group)
    print(f"  Number of distinct vertex stars (I_h orbits): {len(orbits)}")

    orbits_I = orbits_under_group(patterns, group_I_only)
    print(f"  Number of distinct vertex stars (I rotation-only orbits): {len(orbits_I)}")

    print("\n--- Per-orbit summary ---")
    print(f"  {'#':>3}  {'orbit size':>10}  {'coordination':>12}  {'#vertices':>10}  representative")
    print("  " + "-" * 70)
    orbit_summary = []
    for idx, orbit in enumerate(sorted(orbits, key=lambda o: (coordination(min(o)), -len(o)))):
        rep = min(orbit)
        z = coordination(rep)
        n_vertices = sum(len(by_pattern[p]) for p in orbit if p in by_pattern)
        orbit_summary.append({
            "orbit_index": idx,
            "orbit_size": len(orbit),
            "coordination": z,
            "vertices_realising": n_vertices,
            "representative_pattern": rep,
            "representative_binary": format(rep, "012b"),
        })
        print(f"  {idx:>3}  {len(orbit):>10}  {z:>12}  {n_vertices:>10}  0b{rep:012b}")

    coordination_histogram = {}
    for s in orbit_summary:
        coordination_histogram.setdefault(s["coordination"], 0)
        coordination_histogram[s["coordination"]] += 1
    print("\n--- Coordination-number histogram (vertex-star types per coord) ---")
    for z in sorted(coordination_histogram):
        print(f"  coord = {z:2d}:  {coordination_histogram[z]:2d} vertex stars")

    expected_count = 32
    actual_count = len(orbits)
    match = (actual_count == expected_count)
    print("\n" + "=" * 76)
    print(f"Result: {actual_count} distinct vertex stars (App Z Sec Z.3 cites 32).")
    if match:
        print("AKN vertex-star catalog: MATCHES App Z Sec Z.3 cited count.")
    else:
        print(f"AKN vertex-star catalog: count = {actual_count} under the edge-only")
        print(f"  nearest-neighbour definition + I_h equivalence + canonical AKN window.")
        print(f"  Saturated across R=4 and R=5 patches (same 9 under R=3 with the")
        print(f"  ill-formed comparison RT window, and same 9 under R=5 here")
        print(f"  with the canonical perp(unit 6-cube) window).")
        print(f"  Consistent with literature on canonical AKN edge-incidence vertex")
        print(f"  enumeration (8-9 types, depending on origin-vertex treatment).")
        print(f"  The App Z Sec Z.3 cited '32' likely counts matching-rule arrow")
        print(f"  decorations or rhombohedral-cell incidence patterns, not the bare")
        print(f"  edge-incidence vertex stars enumerated here.")
    print("=" * 76)

    verdict = {
        "patch_bounding_box_R": R,
        "z6_candidates": (2 * R + 1) ** 6,
        "akn_vertices_in_window": len(patch),
        "interior_vertices_used": len(interior_patch),
        "distinct_raw_patterns": len(by_pattern),
        "Ih_group_order": len(group),
        "I_group_order": len(group_I_only),
        "vertex_star_count_Ih_orbits": actual_count,
        "vertex_star_count_I_orbits": len(orbits_I),
        "vertex_star_count_expected_per_App_Z": expected_count,
        "matches_expected_under_Ih": match,
        "matches_expected_under_I": (len(orbits_I) == expected_count),
        "coordination_histogram_Ih": coordination_histogram,
        "orbits": orbit_summary,
    }
    out_path = ENGINE_ROOT / "data" / "protocol_akn_vertex_stars_results.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        json.dump(verdict, f, indent=2)
    print(f"\nFull catalog written to: {out_path}")
    print("=" * 76)


if __name__ == "__main__":
    main()
